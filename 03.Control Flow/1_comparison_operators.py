# COMPARISON OPERATORS
x = int(input("Masukkan nilai x: "))
y = int(input("Masukkan nilai y: "))

lebih_besar = x > y
lebih_kecil = x < y
sama_persis = x == y


print(f"1. Apakah nilai x={x} lebih besar dari y={y}? {lebih_besar}")
print(f"2. Apakah nilai x={x} lebih kecil dari y={y}? {lebih_kecil}")
print(f"3. Apakah nilai x={x} sama persis dengan nilai y={y}? {sama_persis}")
